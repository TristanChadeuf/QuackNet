    public function loadUserByIdentifier(string $ducknameOrEmail): ?Duck
    {
        $entityManager = $this->getEntityManager();

        return $entityManager->createQuery(
            'SELECT u
                FROM App\Entity\Duck u
                WHERE u.duckname = :query
                OR u.email = :query'
        )
            ->setParameter('query', $ducknameOrEmail)
            ->getOneOrNullResult();
    }
